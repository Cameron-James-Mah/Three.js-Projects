function App() {
  const idx = useRef(1)

  function copyBoard(arr){
    let temp = [[]]
    for(let i = 0; i < arr.length; i++){
      for(let j = 0; j < arr[0].length; j++){
        if(!temp[i]){
          temp[i] = []
        }
        temp[i][j] = arr[i][j]
      }
    }
    return temp
  }

  function animate(){
    requestAnimationFrame( animate );
	  renderer.render( scene, camera );
  }
  useEffect(() => {
    scene.add(gridHelper)
    scene.add( group )
    //group.clear()
    animate()
    boardCopy = copyBoard(board)
    const interval = setInterval(()=>{   
        group.clear()
        for(let i = 0; i < board.length; i++){
          for(let j  = 0; j < board[0].length; j++){
            //Show live cells
            if(board[i][j] == 1){ 
              const plane = new THREE.Mesh( geometry, material );
              plane.rotation.x = Math.PI / 2
              if(i < 13){
                plane.position.z = (13-i)*(-2)+2
              }
              else{ 
                plane.position.z = (i-13)*(2)+2
              }
              if(j < 13){
                plane.position.x = (13-j)*(-2)+2
              }
              else{ 
                plane.position.x = (j-13)*(2)+2
              }
             group.add(plane)
            }
            //count live adjacent cells
            let cnt = 0
            if(j < 24 && board[i][j+1] == 1){ //right
              cnt++
            }
            if(i < 24 && board[i+1][j] == 1){ //down
              cnt++
            }
            if(i > 0 && board[i-1][j] == 1){ //up
              cnt++
            }
            if(j > 0 && board[i][j-1] == 1){ //left
              cnt++
            }
            if(j > 0 && i < 24 && board[i+1][j-1] == 1){ //left down
              cnt++
            }

            if(j < 24 && i > 0 && board[i-1][j+1] == 1){ //right up
              //console.log(i + " " + j + " ")
              cnt++
            }
            if(j < 24 && i < 24 && board[i+1][j+1] == 1){ //right down
              cnt++
            }
            if(j > 0 && i > 0 && board[i-1][j-1] == 1){ //left up
              cnt++
            }
            if((board[i][j] == 1 && (cnt == 2 || cnt == 3)) || (board[i][j] == 0 && cnt == 3)){
              boardCopy[i][j] = 1
            }
            else{
              boardCopy[i][j] = 0
            }

          }
        }
        board = copyBoard(boardCopy)
        
    }, 300)
    return () =>{
      clearInterval(interval)
    }
    

  }, [])
  
  
  return (
    <>
      
    </>
  );
}

export default App;
